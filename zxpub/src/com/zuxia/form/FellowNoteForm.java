package com.zuxia.form;

public class FellowNoteForm {
	private int noteCd;
	private String content;

	/**
	 * noteCd属性的get方法
	 * 
	 * @return the noteCd
	 */
	public int getNoteCd() {
		return noteCd;
	}

	/**
	 * noteCd属性的set方法
	 * 
	 * @param noteCd
	 *            the noteCd to set
	 */
	public void setNoteCd(int noteCd) {
		this.noteCd = noteCd;
	}

	/**
	 * content属性的get方法
	 * 
	 * @return the content
	 */
	public String getContent() {
		return content;
	}

	/**
	 * content属性的set方法
	 * 
	 * @param content
	 *            the content to set
	 */
	public void setContent(String content) {
		this.content = content;
	}

}
