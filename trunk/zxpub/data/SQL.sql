
/*----------------建数USER(1用户信息)表----------------------*/
DROP TABLE USER;
CREATE TABLE USER
(
   USER_CD INT AUTO_INCREMENT PRIMARY KEY,
   USER_NAME VARCHAR(20) NOT NULL,
   ROLE_CD INT default 0,
   SEX CHAR(4) NOT NULL,
   PHOTO_PATH VARCHAR (100),
   BIRTHDAY DATE,
   PROVINCE_CD INT,
   CITY_CD INT,
   EMAIL VARCHAR (40),
   SCORE INT default 0,
   LAST_LOGIN_DATE DATE,
   REGIST_DATE DATE,
   COMMENT VARCHAR (200)
);

/*----------------建数NOTE(2帖子)表----------------------*/
DROP TABLE NOTE;
CREATE TABLE NOTE
(
NOTE_CD INT AUTO_INCREMENT PRIMARY KEY,
PUB_USER_CD INT,
MODULE_CD INT,
CHILD_MODULE_CD INT,
TITLE VARCHAR(40),
CONTENT VARCHAR (4000),
PUBLISH_DATE DATE,
point int
);

/*----------------建数FELLOW_NOTE(3跟贴)表----------------------*/
DROP TABLE FELLOW_NOTE;
CREATE TABLE FELLOW_NOTE
(
FELLOW_NOTE_CD INT AUTO_INCREMENT PRIMARY KEY,
OWNER_NOTE_CD INT,
MODULE_CD INT,
CHILD_MODULE_CD INT,
FLW_USER_CD INT,
FLW_CONTENT VARCHAR(1000),
PUBLISH_DATE DATE
);

/*----------------建数MODULE(4模块)表----------------------*/
DROP TABLE MODULE;
CREATE TABLE MODULE
(
MODULE_CD INT AUTO_INCREMENT PRIMARY KEY,
MODULE_NAME VARCHAR(40),
MODULE_MASTER_CD INT
);

/*----------------建数CHILD_MODULE(5子模块)表----------------------*/
DROP TABLE CHILD_MODULE;
CREATE TABLE CHILD_MODULE
(
CHILD_MODULE_CD INT AUTO_INCREMENT PRIMARY KEY,
MODULE_CD INT,
CHILD_MODULE_NAME VARCHAR(40),
CHILD_MODULE_MASTER_CD INT,
MODULE_COMMENT VARCHAR(100)
);

/*----------------建数PROVINCE(6省别)表----------------------*/
DROP TABLE PROVINCE;
CREATE TABLE PROVINCE
(
PROVINCE_CD INT AUTO_INCREMENT PRIMARY KEY,
PROVINCE_NAME VARCHAR(20)
);

/*----------------建数CITY(7市别)表----------------------*/
DROP TABLE CITY;
CREATE TABLE CITY
(
CITY_CD INT AUTO_INCREMENT PRIMARY KEY,
PROVINCE_CD INT, 
CITY_NAME VARCHAR(20)
);

/*----------------建数PASSWORD(8密码信息)表----------------------*/
DROP TABLE PASSWORD;
CREATE TABLE PASSWORD
(
USER_CD INT PRIMARY KEY,
PASSWORD VARCHAR(10)
);

/*----------------建数SAFE_QUESTION(9安全问题)表----------------------*/
DROP TABLE SAFE_QUESTION;
CREATE TABLE SAFE_QUESTION
(
USER_CD INT PRIMARY KEY,
QUESTION_CD INT,
ANSWER VARCHAR(50)
);

/*----------------建数QUESTION_MASTER(10安全问题主)表----------------------*/
DROP TABLE QUESTION_MASTER;
CREATE TABLE QUESTION_MASTER
(
QUESTION_CD INT AUTO_INCREMENT PRIMARY KEY,
QUESTION VARCHAR(50)
);
